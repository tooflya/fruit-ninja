void Cutter::onCreate()
{
    Entity::onCreate();
    
    this->setScale(0.0);
    this->runAction(CCScaleTo::create(0.1, 1.0));
}
    
Cutter* Cutter::deepCopy()
{
    return new Cutter();
}
    
void Cutter::update(float pDeltaTime)
{
    Entity::update(pDeltaTime);
    
    if(!this->isVisible()) return;
    
    if(this->getScaleX() >= 1.0)
    {
        this->destroy();
    }
}

